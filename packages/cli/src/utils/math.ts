/**
 * دالة بسيطة لجمع رقمين
 * @param a الرقم الأول
 * @param b الرقم الثاني
 * @returns ناتج الجمع
 */
export function add(a: number, b: number): number {
    return a + b;
}

/**
 * دالة لحساب مربع رقم
 * @param x الرقم المراد حساب مربعه
 * @returns مربع الرقم
 */
export function square(x: number): number {
    return x * x;
}